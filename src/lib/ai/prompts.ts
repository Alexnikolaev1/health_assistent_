export function buildSymptomUserPrompt(symptomText: string, userHistory?: string): string {
  let prompt = `Пользователь описывает симптомы:\n"${symptomText}"`;
  
  if (userHistory) {
    prompt += `\n\nПредыдущие обращения пользователя (для контекста):\n${userHistory}`;
  }
  
  prompt += '\n\nПроанализируй симптомы и дай ответ в требуемом JSON-формате.';
  return prompt;
}
